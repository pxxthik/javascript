console.log("Operators in JS")

// Arithmetic Operators
let a = 45;
let b = 4;
console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a * b = ", a * b)
console.log("a / b = ", a / b)
console.log("a % b = ", a % b)
console.log("a ** b = ", a ** b)
console.log("a++ = ", a++)
console.log("a-- = ", a--)

// Assignment Operators
a = 8
a += 5 // same as a = a + 5
console.log(a)

// Comparision Operator
let comp1 = 6
let comp2 = 7
console.log("comp1 == comp2 is ", comp1 == comp2)
console.log("comp1 != comp2 is ", comp1 != comp2)

console.log("comp1 == comp2 is ", comp1 === comp2)
console.log("comp1 != comp2 is ", comp1 !== comp2)

console.log("comp1 > comp2 is ", comp1 > comp2)

// Logical Operator
let x = 5;
let y = 6;
console.log(x<y && x==y)
console.log(x<y || x==y)
console.log(!(x<y || x==y))
console.log(!false)

